    /**
     * Botón para modificar la fecha de finzalicion de contrato del miembro del
     * cuerpo tecnico
     *
     * @param evt
     */
    private void fechaButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fechaButonActionPerformed

        FactoriaIntegrantes factoria = new FactoriaIntegrantes();
        IntegrantePlantilla integrante = factoria.getIntegrantePlantilla(1);
        MiembroCuerpoTecnicoDAO dao = (MiembroCuerpoTecnicoDAO) integrante.crearIntegrantePostgreSQL();

        try {

            if (id_equipo == dao.obtenerIDEquipo("\'" + DNITxt.getText() + "\'")) {

                dao.modificarF((new Date(Integer.parseInt(FcontratoCombo.getSelectedItem().toString()) - 1900, 5, 30)), DNITxt.getText());
                JOptionPane.showMessageDialog(null, "Fecha fin de contrato modificada correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Este DNI no pertenece a su equipo");
            }

        } catch (DAOException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(null, "Error al modificar");
        }
    }//GEN-LAST:event_fechaButonActionPerformed

