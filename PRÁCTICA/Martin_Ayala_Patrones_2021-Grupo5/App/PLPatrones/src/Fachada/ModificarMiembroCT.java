    /**
     * Botón para modificar el salario del miembro del cuerpo tecnico
     *
     * @param evt
     */
    private void SalarioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalarioButtonActionPerformed

        FactoriaIntegrantes factoria = new FactoriaIntegrantes();
        IntegrantePlantilla integrante = factoria.getIntegrantePlantilla(1);
        MiembroCuerpoTecnicoDAO dao = (MiembroCuerpoTecnicoDAO) integrante.crearIntegrantePostgreSQL();

        try {

            if (id_equipo == dao.obtenerIDEquipo("\'" + DNITxt.getText() + "\'")) {

                dao.modificar(Integer.parseInt(SalarioTxt.getText()), DNITxt.getText());
                JOptionPane.showMessageDialog(null, "Salario modificado correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Este DNI no pertenece a su equipo");
            }

        } catch (DAOException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(null, "Error al modificar");
        }
    }//GEN-LAST:event_SalarioButtonActionPerformed
