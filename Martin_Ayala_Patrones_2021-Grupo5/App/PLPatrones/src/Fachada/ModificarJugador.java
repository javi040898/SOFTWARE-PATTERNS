    /**
     * Bot贸n para modificar el dorsal del jugador
     *
     * @param evt
     */
    private void DorsalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DorsalButtonActionPerformed

        FactoriaIntegrantes factoria = new FactoriaIntegrantes();
        IntegrantePlantilla integrante = factoria.getIntegrantePlantilla(0);
        JugadorDAO dao = (JugadorDAO) integrante.crearIntegrantePostgreSQL();

        try {

            if (id_equipo == dao.obtenerIDEquipo("\'" + DNITxt.getText() + "\'")) {

                if (dao.obtenerDorsal(id_equipo, Integer.parseInt(dorsalCombo.getSelectedItem().toString())) == 1) {
                    JOptionPane.showMessageDialog(null, "Ya existe este dorsal");
                } else {
                    dao.modificarD(Integer.parseInt(dorsalCombo.getSelectedItem().toString()), DNITxt.getText());
                    JOptionPane.showMessageDialog(null, "Dorsal modificado correctamente");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Este DNI no pertenece a su equipo");
            }

        } catch (DAOException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(null, "Error al modificar");
        }
    }//GEN-LAST:event_DorsalButtonActionPerformed
